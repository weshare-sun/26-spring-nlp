def load_custom_corpus(file_path):
    """
    加载自定义中文语料
    每行一个句子
    """
    if not os.path.exists(file_path):
        raise FileNotFoundError(f'语料文件不存在: {file_path}')

    sentences = []
    with open(file_path, 'r', encoding='utf-8') as f:
        for line in f:
            line = line.strip()
            if line:
                sentences.append(line)
    return sentences


