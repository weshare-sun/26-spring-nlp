def download_chinese_wiki(data_dir='data', use_online=True):
    """
    下载中文维基百科数据
    """
    os.makedirs(data_dir, exist_ok=True)
    target = os.path.join(data_dir, 'chinese_corpus.txt')

    if os.path.exists(target):
        # 检查文件大小，太小就重新下载
        size = os.path.getsize(target)
        if size > 10000:  # 大于 10KB 认为是有效语料
            print(f'[download] 中文语料已存在: {target} ({size/1024:.1f}KB)')
            return target

    if use_online:
        # 尝试下载在线语料
        try:
            return download_online_corpus(data_dir)
        except Exception as e:
            print(f'[download] 在线下载失败: {e}')
            print('[download] 使用本地示例语料')

    # 使用扩充的本地示例语料
    return create_sample_corpus(data_dir)


